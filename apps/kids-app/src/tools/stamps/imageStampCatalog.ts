const formatLabel = (assetId: string): string => {
  const withSpaces = assetId.replace(/([a-z])([0-9])/g, "$1 $2");
  return withSpaces.replace(
    /(^|\s)([a-z])/g,
    (_m, p1: string, p2: string) => `${p1}${p2.toUpperCase()}`,
  );
};

export function getImageStampLabel(assetId: string): string {
  return formatLabel(assetId);
}

export function getImageStampAssets(): readonly ImageStampAsset[] {
  return IMAGE_STAMP_ASSETS;
}

export function getImageStampAsset(assetId: string): ImageStampAsset {
  const asset = IMAGE_STAMP_ASSETS.find((item) => item.id === assetId);
  if (!asset) {
    throw new Error(`Unknown image stamp asset ${assetId}`);
  }
  return asset;
}

export function getImageStampToolId(assetId: string): string {
  return `stamp.image.${assetId}`;
}

export function getImageStampAssetIdFromToolId(toolId: string): string | null {
  if (!toolId.startsWith("stamp.image.")) {
    return null;
  }
  return toolId.slice("stamp.image.".length) || null;
}
